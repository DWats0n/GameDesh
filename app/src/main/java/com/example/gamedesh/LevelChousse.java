    private String getBestTime(int Level){

        String message = "";
        FileInputStream file = null;
        try {
            file = openFileInput(FILE_NAME);
            byte[] bytes = new byte[file.available()];
            file.read(bytes);
            String text = new String (bytes);
            System.out.println(text.split("\n\\."));
            for(String s : text.split("\n\\.")){
                String n = "";
                try {
                    n = String.valueOf(s.charAt(0));
                }catch (Exception e){
                    n="-1";
                }
                if(n.equals(String.valueOf(Level))){
                    message = s.substring(1);
                    break;
                }
                else {
                    message = "not passed";
                }
                System.out.println();
            }


        } catch (IOException e) {

        }


        return message;
    }

